function getRandomInt(min: number, max: number) {
    min = Math.ceil(min);
    max = Math.floor(max);
    //The maximum is exclusive and the minimum is inclusive
    return Math.floor(Math.random() * (max - min)) + min;
}
export function randomNotFoundIcons() {
    return notFoundIcons[getRandomInt(0, notFoundIcons.length)];
}
export default function getName() {
    const animal = animals[getRandomInt(0, animals.length)];
    const adjective = adjectives[getRandomInt(0, adjectives.length)];
    return `${adjective} ${animal}`;
}
